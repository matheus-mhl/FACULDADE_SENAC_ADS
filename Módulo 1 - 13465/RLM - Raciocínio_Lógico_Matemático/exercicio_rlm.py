#Exercício 1: Criação de conjuntos A e B
A = {1,2,3,4,5}
B = {4,5,6,7,8}
print('A =', A)
print('B =', B)

#Exercício 2: Operação de União: (A ∪ B)
print('A | B =>', A | B)
print('A.union(B) =>', A.union(B))

#Exercício 3: Operação de Interseção: (A ∩ B)
print('A & B =>', A, B)
print('A.intersection(B) =>', A.intersection(B))

#Exercpicio 4: Operação de Diferença: (A - B) e (B - A)
print('A - B =>', A - B)
print('A.difference(B) =>', A.difference(B))
print('B - A =>', B - A)
print('B.difference(A) =>', B.difference(A)) 

